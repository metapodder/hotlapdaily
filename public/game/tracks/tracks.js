export function getTrackFunction(trackId, centerX, centerY, scale) {
    if (!trackFunctions[trackId]) {
        console.error(`[Tracks] trackId "${trackId}" not found in trackFunctions`);
        return null;
    }
    return trackFunctions[trackId](scale, centerX, centerY) || null;
}

/**
 * Async version that falls back to API fetch for tracks not in the local file.
 * Caches fetched track code in sessionStorage.
 */
export async function getTrackFunctionAsync(trackId, centerX, centerY, scale) {
    // 1. Try sync path first (local hardcoded tracks)
    if (trackFunctions[trackId]) {
        return trackFunctions[trackId](scale, centerX, centerY) || null;
    }

    // Check if loaded via ?track= param (preview/practice mode) — skip cache for fresh data
    const isPreview = new URLSearchParams(window.location.search).has('track');

    // 2. Check sessionStorage cache (skip in preview mode)
    const cacheKey = `hotlapdaily_track_${trackId}`;
    let trackCode = null;
    if (!isPreview) {
        try {
            trackCode = sessionStorage.getItem(cacheKey);
        } catch { /* sessionStorage unavailable */ }
    }

    // 3. Fetch from API if not cached
    if (!trackCode) {
        const fetchUrl = isPreview
            ? `/api/track?trackId=${trackId}&_t=${Date.now()}`
            : `/api/track?trackId=${trackId}`;
        const res = await fetch(fetchUrl, isPreview ? { cache: 'no-store' } : undefined);
        if (!res.ok) {
            console.error(`[Tracks] API returned ${res.status} for trackId ${trackId}`);
            return null;
        }
        const data = await res.json();
        trackCode = data.trackFunction;
        if (!trackCode) return null;

        // Cache in sessionStorage (update cache even in preview mode so daily play gets latest)
        try {
            sessionStorage.setItem(cacheKey, trackCode);
        } catch { /* sessionStorage full or unavailable */ }
    }

    // 4. Evaluate and call with params
    try {
        const func = new Function('return ' + trackCode)();
        if (typeof func !== 'function') return null;
        return func(scale, centerX, centerY) || null;
    } catch (err) {
        console.error(`[Tracks] Failed to evaluate track code for ${trackId}:`, err);
        return null;
    }
}
